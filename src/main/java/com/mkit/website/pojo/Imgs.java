package com.mkit.website.pojo;

public class Imgs {
	private String h;
	private String url;
	private String w;
	
	
	
	
	
	public String getH() {
		return h;
	}





	public void setH(String h) {
		this.h = h;
	}





	public String getUrl() {
		return url;
	}





	public void setUrl(String url) {
		this.url = url;
	}





	public String getW() {
		return w;
	}





	public void setW(String w) {
		this.w = w;
	}





	@Override
	public String toString() {
		return "Imgs [h=" + h + ", url=" + url + ", w=" + w + "]";
	}
	

}
